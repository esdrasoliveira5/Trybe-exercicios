
function addition(valueA, valueB) {
    let result = valueA + valueB;
    return result;
}

function subtraction(valueA, valueB) {
    let result = valueA - valueB;
    return result;
}

function multiplication(valueA, valueB) {
    let result = valueA * valueB;
    return result;
}

function division(valueA, valueB) {
    let result = valueA / valueB;
    return result;
}

function modulo(valueA, valueB) {
    let result = valueA % valueB;
    return result;
}
